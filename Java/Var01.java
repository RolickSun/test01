//变量
public class Var01{
    public static void main(String[] args) {
        int a = 800;    //整数       
        double b = 88.6; //双浮点  
        char gender = 'a';  //单字符   
        String name = "quin"; //字符串
        System.out.println(a);
        System.out.println(b);
        System.out.println(gender);
        System.out.println(name);
    }
}